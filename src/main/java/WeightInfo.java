public class WeightInfo {
    public Float weight;
    public String start;
    public String end;
    public String transRid;
    public String edgeRid;

    public Float getWeight() {
        return weight;
    }

    public void setWeight(Float weight) {
        this.weight = weight;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public String getTransRid() {
        return transRid;
    }

    public void setTransRid(String transRid) {
        this.transRid = transRid;
    }

    public String getEdgeRid() {
        return edgeRid;
    }

    public void setEdgeRid(String edgeRid) {
        this.edgeRid = edgeRid;
    }
}
